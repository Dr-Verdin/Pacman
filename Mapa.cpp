#include <iostream>
#include <vector>

using namespace std;

// Função para imprimir a matriz
void imprimirMatriz(const vector<vector<int>>& matrix) {
    for (const auto& linha : matrix) {
        for (int valor : linha) {
            cout << valor << " ";
        }
        cout << endl;
    }
}

// Função para verificar se uma posição (x, y) é válida
bool ehPosicaoValida(int x, int y, const vector<vector<int>>& matrix) {
    return (x < matrix.size() && y < matrix[0].size() && matrix[x][y] == 0);
}

